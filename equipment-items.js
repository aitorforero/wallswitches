/**
 * Item names of the equipment
 */
class EquipmentItems {
    /**
     * Equipment item name.
     * @type {string}
     */
    equipment;

    /**
     * Left button status item name.
     * @type {string}
     */
    left;

    /**
     * Right button status item name.
     * @type {string}
     */
    right;

    /**
     * Both buttons status item name.
     * @type {string}
     */
    both;

    /**
     * Equipment state item name.
     * @type {string}
     */
    status;

    /**
     * Equipment initialization command item name.
     * @type {string}
     */
    initialize;
}

module.exports = EquipmentItems;

