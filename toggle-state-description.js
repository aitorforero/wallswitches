/**
 * Description of the 2 toggle state
 */
class ToggleStateDescription {
    /**
     * Id of the state
     * @type string
     */
    id;

    /**
     * Text of the state
     * @type string
     */
    text;

    /**
     * Name of the item to toggle on left
     * @type string
     */
    leftItemName;

    /**
     * Name of the item to toggle on right
     * @type string
     */
    rightItemName;
}

module.exports = {ToggleStateDescription};